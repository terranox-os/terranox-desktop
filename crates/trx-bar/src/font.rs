/// Draw a string into an ARGB pixel buffer at (x, y) with given color.
/// Alpha blending at 75% opacity for smoother text.
pub fn draw_text(buf: &mut [u32], stride: usize, height: usize, x: i32, y: i32, color: u32, text: &str) {
    let r = (color >> 16) & 0xFF;
    let g = (color >> 8) & 0xFF;
    let b = color & 0xFF;
    let alpha = 192u32; // 75% opacity

    let mut pen_x = x;
    for ch in text.bytes() {
        let idx = if ch >= 32 && ch <= 126 { (ch - 32) as usize } else { b'?' as usize - 32 };
        let glyph = &GLYPHS[idx];

        for row in 0..GLYPH_H {
            let dy = y as usize + row;
            if dy >= height { continue; }
            let bits = glyph[row];
            for col in 0..6 {
                if bits & (0x20 >> col) == 0 { continue; }
                let dx = pen_x as usize + col;
                if dx >= stride { continue; }

                let bg = buf[dy * stride + dx];
                let bg_r = (bg >> 16) & 0xFF;
                let bg_g = (bg >> 8) & 0xFF;
                let bg_b = bg & 0xFF;

                let out_r = (r * alpha + bg_r * (255 - alpha)) / 255;
                let out_g = (g * alpha + bg_g * (255 - alpha)) / 255;
                let out_b = (b * alpha + bg_b * (255 - alpha)) / 255;
                buf[dy * stride + dx] = 0xFF00_0000 | (out_r << 16) | (out_g << 8) | out_b;
            }
        }
        pen_x += GLYPH_W as i32;
    }
}

/// Return the pixel width of a string.
pub fn text_width(text: &str) -> i32 {
    text.len() as i32 * GLYPH_W as i32
}
